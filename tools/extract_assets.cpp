// Extract a single face piece as a standalone sprite (column-major bottom-up → row-major top-down)
Sprite extractPiece(const uint8_t* data, int hBytes, int wCols) {
    Sprite s;
    s.w = wCols;
    s.h = hBytes * 2;
    s.cx = wCols / 2;
    s.cy = s.h / 2;
    s.pixels.resize(s.w * s.h, 0);
    for (int col = 0; col < wCols; col++) {
        for (int b = 0; b < hBytes; b++) {
            uint8_t byte = data[col * hBytes + b];
            uint8_t hi = (byte >> 4) & 0xF;
            uint8_t lo = byte & 0xF;
            int yHi = s.h - 1 - (2 * b);
            int yLo = s.h - 1 - (2 * b + 1);
            if (yHi >= 0 && yHi < s.h && hi) s.pixels[yHi * s.w + col] = hi;
            if (yLo >= 0 && yLo < s.h && lo) s.pixels[yLo * s.w + col] = lo;
        }
    }
    return s;
}

