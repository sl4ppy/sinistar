// Williams arcade palette (BBGGGRRR format) -> RGBA
static uint32_t palette[16];
static void initPalette() {
    static const uint8_t pal[] = {
        0x00,0xFF,0xBF,0xAE,0xAD,0xA4,0x9A,0x00,
        0x00,0xC9,0x50,0x4B,0x05,0x07,0x00,0x37
    };
    for (int i = 0; i < 16; i++) {
        uint8_t v = pal[i];
        int r3 = v & 7, g3 = (v >> 3) & 7, b2 = (v >> 6) & 3;
        uint8_t r = (r3 << 5) | (r3 << 2) | (r3 >> 1);
        uint8_t g = (g3 << 5) | (g3 << 2) | (g3 >> 1);
        uint8_t b = (b2 << 6) | (b2 << 4) | (b2 << 2) | b2;
        palette[i] = ((uint32_t)r) | ((uint32_t)g << 8) | ((uint32_t)b << 16) | 0xFF000000u;
        if (i == 0) palette[i] = 0; // index 0 = transparent
    }
}

struct Sprite {
    int w = 0, h = 0, cx = 0, cy = 0;
    std::vector<uint8_t> pixels;
};

struct SpriteSet {
    std::vector<Sprite> frames;
};

// Descriptor addresses from original ROM analysis
static constexpr uint16_t DESC_PLAYER  = 0x0007;
static constexpr uint16_t DESC_WORKER  = 0x129D;
static constexpr uint16_t DESC_WARRIOR = 0x19E1;
static constexpr uint16_t DESC_PLAN1   = 0x0B23;
static constexpr uint16_t DESC_PLAN2   = 0x0CCB;
static constexpr uint16_t DESC_PLAN3   = 0x0E3B;
static constexpr uint16_t DESC_PLAN4   = 0x0F1B;
static constexpr uint16_t DESC_PLAN5   = 0x109D;
