// === Output: PNG sprite sheet + metadata ===

void saveSpriteSheet(const char* pngPath, const char* metaPath,
                     const std::vector<Sprite>& frames) {
    // Find max cell size
    int cellW = 0, cellH = 0;
    for (auto& f : frames) { cellW = std::max(cellW, f.w); cellH = std::max(cellH, f.h); }
    int nframes = (int)frames.size();
    int sheetW = cellW * nframes, sheetH = cellH;

    // Create RGBA image
    std::vector<uint32_t> img(sheetW * sheetH, 0); // transparent
    for (int fi = 0; fi < nframes; fi++) {
        auto& f = frames[fi];
        int ox = fi * cellW;
        for (int y = 0; y < f.h; y++) {
            for (int x = 0; x < f.w; x++) {
                uint8_t idx = f.pixels[y * f.w + x];
                if (idx == 0) continue;
                img[(y) * sheetW + (ox + x)] = palette[idx];
            }
        }
    }

    stbi_write_png(pngPath, sheetW, sheetH, 4, img.data(), sheetW * 4);

    // Write metadata
    FILE* meta = fopen(metaPath, "w");
    if (meta) {
        fprintf(meta, "# Sinistar sprite sheet metadata\n");
        fprintf(meta, "# nframes cellw cellh\n");
        fprintf(meta, "%d %d %d\n", nframes, cellW, cellH);
        fprintf(meta, "# per-frame: w h cx cy\n");
        for (auto& f : frames)
            fprintf(meta, "%d %d %d %d\n", f.w, f.h, f.cx, f.cy);
        fclose(meta);
    }
    printf("  %s (%dx%d, %d frames, cell %dx%d)\n", pngPath, sheetW, sheetH, nframes, cellW, cellH);
}

void copyFile(const char* src, const char* dst) {
    FILE* in = fopen(src, "rb");
    if (!in) { fprintf(stderr, "Warning: cannot open %s\n", src); return; }
    FILE* out = fopen(dst, "wb");
    if (!out) { fclose(in); fprintf(stderr, "Warning: cannot create %s\n", dst); return; }
    uint8_t buf[4096];
    size_t n;
    while ((n = fread(buf, 1, sizeof(buf), in)) > 0)
        fwrite(buf, 1, n, out);
    fclose(in);
    fclose(out);
}

#ifdef _WIN32
#include <direct.h>
#define MKDIR(d) _mkdir(d)
#else
#include <sys/stat.h>
#define MKDIR(d) mkdir(d, 0755)
#endif

void mkdirs(const char* path) {
    char tmp[512];
    snprintf(tmp, sizeof(tmp), "%s", path);
    for (char* p = tmp + 1; *p; p++) {
        if (*p == '/' || *p == '\\') {
            *p = 0;
            MKDIR(tmp);
            *p = '/';
        }
    }
    MKDIR(tmp);
}

int main() {
    // Load game ROMs
    for (int i = 0; i < NUM_GAME_ROMS; i++) {
        char path[256];
        snprintf(path, sizeof(path), "source/sinistar.%02d", i + 1);
        FILE* f = fopen(path, "rb");
        if (!f) { fprintf(stderr, "Cannot open %s\n", path); return 1; }
        if (fread(&romData[i * ROM_SIZE], 1, ROM_SIZE, f) != ROM_SIZE) {
            fprintf(stderr, "Short read: %s\n", path); fclose(f); return 1;
        }
        fclose(f);
    }
    printf("Loaded %d ROM files\n", NUM_GAME_ROMS);

    initPalette();

    // Extract all sprites
    SpriteSet player   = extractSet(DESC_PLAYER, 32);
    SpriteSet worker   = extractSet(DESC_WORKER, 16);
    SpriteSet warrior  = extractSet(DESC_WARRIOR, 1);
    SpriteSet sinibomb = extractSet(DESC_SBOMB, 3);
    SpriteSet shotSet  = extractSet(DESC_SHOT, 16);

    Sprite planetoids[5];
    uint16_t planAddrs[] = {DESC_PLAN1, DESC_PLAN2, DESC_PLAN3, DESC_PLAN4, DESC_PLAN5};
    for (int i = 0; i < 5; i++) planetoids[i] = extractOne(planAddrs[i]);

    Sprite crystal = extractOne(DESC_CRYSTAL);
    Sprite sinistarFace = makeSinistarFace();

    // Create output directories
    mkdirs("assets/sprites");
    mkdirs("assets/speech");

    // Save sprite sheets
    printf("Saving sprite sheets:\n");
    saveSpriteSheet("assets/sprites/player.png", "assets/sprites/player.meta", player.frames);
    saveSpriteSheet("assets/sprites/worker.png", "assets/sprites/worker.meta", worker.frames);
    saveSpriteSheet("assets/sprites/warrior.png", "assets/sprites/warrior.meta", warrior.frames);
    saveSpriteSheet("assets/sprites/sinibomb.png", "assets/sprites/sinibomb.meta", sinibomb.frames);
    saveSpriteSheet("assets/sprites/shots.png", "assets/sprites/shots.meta", shotSet.frames);

    // Planetoids: 5 different sprites -> treat as 5-frame sheet
    std::vector<Sprite> planVec(planetoids, planetoids + 5);
    saveSpriteSheet("assets/sprites/planetoid.png", "assets/sprites/planetoid.meta", planVec);

    saveSpriteSheet("assets/sprites/crystal.png", "assets/sprites/crystal.meta", {crystal});
    saveSpriteSheet("assets/sprites/sinistar_face.png", "assets/sprites/sinistar_face.meta", {sinistarFace});

    // Extract individual Sinistar face pieces for piece-by-piece rendering
    SpriteSet siniPieces = extractSinistarPieces();
    saveSpriteSheet("assets/sprites/sinistar_pieces.png", "assets/sprites/sinistar_pieces.meta", siniPieces.frames);

    // Copy speech WAVs
    printf("Copying speech WAV files:\n");
    static const char* wavNames[] = {
        "bewareil", "ihunger", "iamsinis", "runcowar",
        "bewareco", "ihungerc", "runrunru", "aargh"
    };
    for (int i = 0; i < 8; i++) {
        char src[256], dst[256];
        snprintf(src, sizeof(src), "source/speech/%s.wav", wavNames[i]);
        snprintf(dst, sizeof(dst), "assets/speech/%s.wav", wavNames[i]);
        copyFile(src, dst);
        printf("  %s\n", dst);
    }

    // Write palette reference
    FILE* palFile = fopen("assets/sprites/palette.txt", "w");
    if (palFile) {
        fprintf(palFile, "# Sinistar 16-color palette (Williams BBGGGRRR format)\n");
        fprintf(palFile, "# index R G B hex\n");
        for (int i = 0; i < 16; i++) {
            uint32_t c = palette[i];
            uint8_t r = c & 0xFF, g = (c >> 8) & 0xFF, b = (c >> 16) & 0xFF;
            fprintf(palFile, "%2d %3d %3d %3d #%02x%02x%02x\n", i, r, g, b, r, g, b);
        }
        fclose(palFile);
        printf("  assets/sprites/palette.txt\n");
    }

    printf("\nDone! Assets written to assets/ directory.\n");
    printf("Edit any PNG in an image editor - the game loads them at runtime.\n");
    return 0;
}
